"""Built-in Word paragraph style identifiers."""

from typing import Final


class Style:
    NORMAL: Final = "Normal"
    HEADING1: Final = "Heading1"
    HEADING2: Final = "Heading2"
    HEADING3: Final = "Heading3"
    HEADING4: Final = "Heading4"
    HEADING5: Final = "Heading5"
    HEADING6: Final = "Heading6"
    TITLE: Final = "Title"
    SUBTITLE: Final = "Subtitle"
    QUOTE: Final = "Quote"
    INTENSE_QUOTE: Final = "IntenseQuote"
    LIST_PARAGRAPH: Final = "ListParagraph"
    BODY_TEXT: Final = "BodyText"
    CAPTION: Final = "Caption"
    NO_SPACING: Final = "NoSpacing"
